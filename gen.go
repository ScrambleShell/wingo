// +build ignore

// This program generates datafiles.go
package main

import (
	"log"
	"os"
	"text/template"
	"time"
)

func main() {
	f, err := os.Create("datafiles.go")
	die(err)
	defer f.Close()

	packageTemplate.Execute(f, struct {
		Timestamp time.Time
	}{
		Timestamp: time.Now(),
	})
}

func die(err error) {
	if err != nil {
		log.Fatal(err)
	}
}

var packageTemplate = template.Must(template.New("").Parse(`// Code generated by go generate; DO NOT EDIT.
// These are data files built in to wingo.
// {{ .Timestamp }}
package main
`))
